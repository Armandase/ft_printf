#include "ft_printf.h"

void	ft_putstr(char	*s, size_t	*ret)
{
	int	i;

	if (!s)
	{
		write(1, "(null)", 6);
		*ret = *ret + 6;
		return ;
	}
	i = 0;
	while (s[i])
	{
		write(1, &s[i++], 1);
		*ret = *ret + 1;
	}
}
